echo "🔴 崩溃根因分析"
echo "------------------------------------------------------------"
echo ""
echo "1️⃣ GO2SE平台迭代 — 8次连续超时"
echo "   根因: 任务设计 > timeout上限 (303s > 300s)"
echo "   - 任务包含: 检查服务+git+代码分析+迭代+提交"
