# ── 2. 超时分析 ───────────────────────────────────────────────
echo ""
echo "⏱️  超时风险分析"
echo "------------------------------------------------------------"
printf "%-40s %-12s %-10s %-10s %-10s\n" "Cron Job" "Timeout" "实测" "状态" "连续错误"
printf "%-40s %-12s %-10s %-10s %-10s\n" "GO2SE-CEO-管理" "无限制" "88s" "✅ OK" "0"
printf "%-40s %-12s %-10s %-10s %-10s\n" "GO2SE平台迭代" "300s ⚠️" "303s" "🔴 超时" "8 ⚠️⚠️"
printf "%-40s %-12s %-10s %-10s %-10s\n" "市场情报收集" "300s" "52s" "✅ OK" "0"
printf "%-40s %-12s %-10s %-10s %-10s\n" "Capability-Evolver" "300s ⚠️" "300s" "🔴 边界" "3 ⚠️"
printf "%-40s %-12s %-10s %-10s %-10s\n" "OpenClaw每日备份" "120s ⚠️" "120s" "🔴 边界" "1"

# ── 3. 算力分配矩阵 ──────────────────────────────────────────
echo ""
echo "⚡ 算力分配矩阵 (每30分钟周期)"
echo "------------------------------------------------------------"
printf "%-40s %-10s %-10s %-10s\n" "Cron Job" "模式" "30min占比" "每日总时长"
